    private void nuevoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nuevoBtnActionPerformed
        // TODO add your handling code here:
        ControladorCorreo cc = new ControladorCorreo();
        EnviarMensaje em = new EnviarMensaje();
        JComboBox combo = em.cuentasCb;

        for (Credencial credencial : credenciales) {
            String correo = credencial.getEmail();
            combo.addItem(correo);
        }
        em.enviarBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                String cuenta = combo.getSelectedItem().toString();
                for (Credencial credencial : credenciales) {
                    String correo = credencial.getEmail();
                    String pass = credencial.getPassword();
                    String tipoCuenta = credencial.getTipoCuenta();
                    if (cuenta.equals(correo)) {
                        String asunto = em.asuntoTf.getText();
                        String mensaje = em.mensajeTa.getText();
                        String destino = em.paraTf.getText();
                        boolean response = cc.enviarCorreo(correo, pass, tipoCuenta, destino, asunto, mensaje);

                        if (response = true) {
                            JOptionPane.showMessageDialog(null, "MENSAJE ENVIADO CON EXITO");

                            //System.exit(0);
                        }
                        if (response = false) {
                            JOptionPane.showMessageDialog(null, "HA OCURRIDO UN ERROR");
                        }

                    }
                }
            }
        });

        em.setVisible(true);

    }//GEN-LAST:event_nuevoBtnActionPerformed

    private void inboxBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inboxBtnActionPerformed
        // TODO add your handling code here:
        nuevoBtn.setEnabled(true);
        ControladorCorreo cc = new ControladorCorreo();
        List<Mensaje> emails = new ArrayList();
        for (Credencial c : credenciales) {
            String correo = c.getEmail();
            String pass = c.getPassword();
            String tipoCuenta = c.getTipoCuenta();

            if ("Gmail".equals(tipoCuenta)) {
                emails.addAll(cc.leerCorreoGmail(correo, pass));
            }
            if ("Hotmail-Outlook".equals(tipoCuenta)) {
                emails.addAll(cc.leerCorreoHotmail(correo, pass));
            }
        }
        emails.sort(Comparator.comparing(Mensaje::getFechaRecibido).reversed());

        String matriz[][] = new String[emails.size()][4];
        for (int i = 0; i < emails.size(); i++) {
            matriz[i][0] = emails.get(i).getAsunto();
            matriz[i][1] = emails.get(i).getFrom();
            matriz[i][2] = emails.get(i).getTo();
            matriz[i][3] = emails.get(i).getFechaRecibido();

        }
        mensajesTb.setModel(new DefaultTableModel(
                matriz, new String[]{
                    "Asunto", "Enviado por:", "Cuenta", "Fecha de Recibido"
                }
        ));

        mensajesTb.addMouseListener(new java.awt.event.MouseAdapter() {

            public void mouseClicked(java.awt.event.MouseEvent evt) {
                ControladorMensaje cm = new ControladorMensaje();
                int seleccion = mensajesTb.rowAtPoint(evt.getPoint());
                String asunto = String.valueOf(mensajesTb.getValueAt(seleccion, 0));
                String de = String.valueOf(mensajesTb.getValueAt(seleccion, 1));
                String cuenta = String.valueOf(mensajesTb.getValueAt(seleccion, 2));
                String fecha = String.valueOf(mensajesTb.getValueAt(seleccion, 3));

                for (Mensaje mensaje : emails) {
                    String correo = mensaje.getTo();
                    String from = mensaje.getFrom();
                    String date = mensaje.getFechaRecibido();
                    String subject = mensaje.getAsunto();

                    if (cuenta.equals(correo) && de.equals(from) && fecha.equals(date) && asunto.equals(subject)) {
                        Part contenido = mensaje.getContenido();
                        cm.analizaParteDeMensaje(contenido);

                    }

                }

            }

        });
        
        activarBtn.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                ControladorSms sms=new ControladorSms();
                String telefono = celularTf.getText();
                for (Mensaje mensaje : emails) {
                    String asunto = filtroTf.getText();
                    String subject = mensaje.getAsunto();
                    String palabra[]=subject.split("\\s");
                    boolean respuesta;
                    
                    for(int i=0;i<palabra.length;i++)

                    if (asunto.equals(palabra[i])) {
                       respuesta=true;           
                    }
                    if(respuesta=true){
                        try {
                            sms.enviarSMS(telefono);
                        } catch (Exception ex) {
                            ex.printStackTrace();
                        }
                        
                    }
                }
               
            }
        });
    }//GEN-LAST:event_inboxBtnActionPerformed

