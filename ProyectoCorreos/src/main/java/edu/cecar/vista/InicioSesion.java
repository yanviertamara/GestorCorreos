    private void agregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBtnActionPerformed
        // TODO add your handling code here:

        Credencial credencial = new Credencial();
        String correo = "";
        String pass = "";
        String tipoCuenta = "";

        correo = correoTf.getText();
        pass = passTf.getText();
        tipoCuenta = tipoCuentaCb.getSelectedItem().toString();

        if ("".equals(correo) || "".equals(pass) || "".equals(tipoCuenta) || tipoCuentaCb.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Hay campos vacios");
        } else {
            inboxBtn.setEnabled(true);

            credencial.setEmail(correo);
            credencial.setPassword(pass);
            credencial.setTipoCuenta(tipoCuenta);
            credenciales.add(credencial);

        }
    }//GEN-LAST:event_agregarBtnActionPerformed

